// Функция для генерации всех возможных комбинаций
export function generateAllPages(): SEOPageConfig[] {
  const pages: SEOPageConfig[] = []
  let id = 1
  
  const deliveryKeys = Object.keys(deliveryMethods) as DeliveryMethod[]
  const clientKeys = Object.keys(clientStatuses) as ClientStatus[]
  const featureKeys = Object.keys(problemFeatures) as ProblemFeature[]
  const amountKeys = Object.keys(loanAmounts) as LoanAmount[]
  
  // Генерация комбинаций
  for (const delivery of deliveryKeys) {
    // Базовые страницы (только способ получения + сумма)
    for (const amount of amountKeys) {
      const slug = generateSlug({ deliveryMethod: delivery, loanAmount: amount })
      const template = determineTemplate()
      
      pages.push({
        id: String(id++),
        slug,
        template,
        deliveryMethod: delivery,
        loanAmount: amount,
        h1: generateH1({ deliveryMethod: delivery, loanAmount: amount }),
        title: `Займ ${deliveryMethods[delivery].name} ${loanAmounts[amount].name} — оформить онлайн`,
        description: `Получите займ ${deliveryMethods[delivery].name.toLowerCase()} ${loanAmounts[amount].name.toLowerCase()}. Быстрое оформление, мгновенное зачисление.`,
        keywords: [],
        lsi: generateLSI({ deliveryMethod: delivery, loanAmount: amount }),
        content: {
          intro: CONTENT_TEMPLATES.intros[delivery],
          mfoBlock: 'Список МФО',
          ratesTable: 'Таблица ставок',
          specialText: '',
          requirements: CONTENT_TEMPLATES.requirements.general,
          faq: [],
          conclusion: '',
        },
      })
    }
    
    // Страницы с клиентским статусом
    for (const client of clientKeys) {
      for (const amount of amountKeys) {
        const slug = generateSlug({ deliveryMethod: delivery, clientStatus: client, loanAmount: amount })
        const template = determineTemplate(client)
        
        pages.push({
          id: String(id++),
          slug,
          template,
          deliveryMethod: delivery,
          clientStatus: client,
          loanAmount: amount,
          h1: generateH1({ deliveryMethod: delivery, clientStatus: client, loanAmount: amount }),
          title: `Займ ${deliveryMethods[delivery].name} ${clientStatuses[client].name.toLowerCase()} ${loanAmounts[amount].name} — оформить онлайн`,
          description: `Специальный займ ${deliveryMethods[delivery].name.toLowerCase()} для ${clientStatuses[client].name.toLowerCase()}. Льготные условия, быстрое одобрение.`,
          keywords: [],
          lsi: generateLSI({ deliveryMethod: delivery, clientStatus: client, loanAmount: amount }),
          content: {
            intro: CONTENT_TEMPLATES.intros[delivery],
            mfoBlock: 'Список МФО',
            ratesTable: 'Таблица ставок',
            specialText: CONTENT_TEMPLATES.specialTexts[client] || '',
            requirements: CONTENT_TEMPLATES.requirements.general,
            faq: [],
            conclusion: '',
          },
        })
      }
    }
    
    // Страницы с особенностями
    for (const feature of featureKeys) {
      for (const amount of amountKeys) {
        const slug = generateSlug({ deliveryMethod: delivery, problemFeature: feature, loanAmount: amount })
        const template = determineTemplate(undefined, feature)
        
        pages.push({
          id: String(id++),
          slug,
          template,
          deliveryMethod: delivery,
          problemFeature: feature,
          loanAmount: amount,
          h1: generateH1({ deliveryMethod: delivery, problemFeature: feature, loanAmount: amount }),
          title: `Займ ${deliveryMethods[delivery].name} ${problemFeatures[feature].name.toLowerCase()} ${loanAmounts[amount].name} — оформить онлайн`,
          description: `Займ ${deliveryMethods[delivery].name.toLowerCase()} ${problemFeatures[feature].name.toLowerCase()}. ${loanAmounts[amount].name.toLowerCase()}. Быстрое оформление.`,
          keywords: [],
          lsi: generateLSI({ deliveryMethod: delivery, problemFeature: feature, loanAmount: amount }),
          content: {
            intro: CONTENT_TEMPLATES.intros[delivery],
            mfoBlock: 'Список МФО',
            ratesTable: 'Таблица ставок',
            specialText: '',
            requirements: feature === 'no-docs' ? CONTENT_TEMPLATES.requirements.noDocs : 
                         feature === 'bad-ci' ? CONTENT_TEMPLATES.requirements.badCI : 
                         CONTENT_TEMPLATES.requirements.general,
            faq: [],
            conclusion: '',
          },
        })
      }
    }
  }
  
  return pages
}
