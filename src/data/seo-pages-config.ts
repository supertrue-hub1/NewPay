// Интерфейс страницы
export interface SEOPageConfig {
  id: string
  slug: string
  template: PageTemplate
  
  // Переменные
  deliveryMethod: DeliveryMethod
  clientStatus?: ClientStatus
  problemFeature?: ProblemFeature
  ageFilter?: AgeFilter
  loanAmount?: LoanAmount
  
  // SEO данные
  h1: string
  title: string
  description: string
  keywords: string[]
  lsi: string[]
  
  // Структура контента
  content: {
    intro: string
    mfoBlock: string // Заглушка для компонента списка МФО
    ratesTable: string // Описание таблицы ставок
    specialText: string // Текст о льготах для льготных групп
    requirements: string // Требования и документы
    faq: { question: string; answer: string }[]
    conclusion: string
  }
}

// Функция определения шаблона по параметрам
export function determineTemplate(
  clientStatus?: ClientStatus,
  problemFeature?: ProblemFeature
): PageTemplate {
  // Лендинг - для льготных категорий и проблемных запросов
  if (clientStatus || problemFeature === 'bad-ci' || problemFeature === 'delinquencies' || problemFeature === 'bankruptcy') {
    return 'landing'
  }
  
  // Статья - для образовательных запросов
  if (problemFeature === 'no-refusal' || problemFeature === 'no-docs') {
    return 'article'
  }
  
  // Агрегатор - по умолчанию для способов получения
  return 'aggregator'
}

// Функция генерации URL-слага
export function generateSlug(params: {
  deliveryMethod: DeliveryMethod
  clientStatus?: ClientStatus
  problemFeature?: ProblemFeature
  ageFilter?: AgeFilter
  loanAmount?: LoanAmount
}): string {
  const parts: string[] = []
  
  if (params.deliveryMethod) parts.push(deliveryMethods[params.deliveryMethod].slug)
  if (params.clientStatus) parts.push(clientStatuses[params.clientStatus].slug)
  if (params.problemFeature) parts.push(problemFeatures[params.problemFeature].slug)
  if (params.ageFilter) parts.push(ageFilters[params.ageFilter].slug)
  if (params.loanAmount) parts.push(loanAmounts[params.loanAmount].slug)
  
  return parts.join('-')
}

// Функция генерации H1
export function generateH1(params: {
  deliveryMethod: DeliveryMethod
  clientStatus?: ClientStatus
  problemFeature?: ProblemFeature
  ageFilter?: AgeFilter
  loanAmount?: LoanAmount
}): string {
  const parts: string[] = ['Займ']
  
  // Способ получения
  parts.push(deliveryMethods[params.deliveryMethod].name)
  
  // Статус клиента
  if (params.clientStatus) {
    parts.push(clientStatuses[params.clientStatus].name)
  }
  
  // Особенность
  if (params.problemFeature) {
    parts.push(problemFeatures[params.problemFeature].name)
  }
  
  // Возраст
  if (params.ageFilter) {
    parts.push(ageFilters[params.ageFilter].name)
  }
  
  // Сумма
  if (params.loanAmount) {
    parts.push(loanAmounts[params.loanAmount].name)
  }
  
  return parts.join(' ')
}

// Генерирует LSI-фразы для страницы
export function generateLSI(params: {
  deliveryMethod: DeliveryMethod
  clientStatus?: ClientStatus
  problemFeature?: ProblemFeature
  ageFilter?: AgeFilter
  loanAmount?: LoanAmount
}): string[] {
  const lsi: string[] = []
  
  // LSI из способа получения
  lsi.push(...deliveryMethods[params.deliveryMethod].lsi)
  
  // LSI из статуса клиента
  if (params.clientStatus) {
    lsi.push(...clientStatuses[params.clientStatus].lsi)
  }
  
  // LSI из особенности
  if (params.problemFeature) {
    lsi.push(...problemFeatures[params.problemFeature].lsi)
  }
  
  // LSI из возраста
  if (params.ageFilter) {
    lsi.push(...ageFilters[params.ageFilter].lsi)
  }
  
  // LSI из суммы
  if (params.loanAmount) {
    lsi.push(...loanAmounts[params.loanAmount].lsi)
  }
  
  // Общие LSI для микрофинансов
  const commonLSI = [
    'микрозайм',
    'деньги в долг',
    'ссуда до зарплаты',
    'микрофинансовая организация',
    'МФО',
    'быстрый займ',
    'онлайн займ',
    'срочный займ',
    'займ без проверки',
    'заявка на займ',
    'получить займ',
    'взять займ',
  ]
  
  return [...new Set([...lsi, ...commonLSI])]
}

